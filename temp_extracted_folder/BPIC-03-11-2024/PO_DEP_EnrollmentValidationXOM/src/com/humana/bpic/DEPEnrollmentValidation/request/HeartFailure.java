public class HeartFailure {
	private boolean hasFluidInHeart;
	private boolean hasHeartFailure;
	private boolean hasLegSwelling;
	public boolean isHasFluidInHeart() {
		return hasFluidInHeart;
	}
	public void setHasFluidInHeart(boolean hasFluidInHeart) {
		this.hasFluidInHeart = hasFluidInHeart;
	}
	public boolean isHasHeartFailure() {
		return hasHeartFailure;
	}
	public void setHasHeartFailure(boolean hasHeartFailure) {
		this.hasHeartFailure = hasHeartFailure;
	}
	public boolean isHasLegSwelling() {
		return hasLegSwelling;
	}
	public void setHasLegSwelling(boolean hasLegSwelling) {
		this.hasLegSwelling = hasLegSwelling;
	}

}

