package com.humana.bpic.DEPEnrollmentValidation.request;

public class ChronicConditions {
	private CardiovascularDisease cardiovascularDisease ;
	private HeartFailure heartFailure ;
	private LungDisease lungDisease ;
	private Diabetes diabetes ;
	public CardiovascularDisease getCardiovascularDisease() {
		return cardiovascularDisease;
	}
	public void setCardiovascularDisease(CardiovascularDisease cardiovascularDisease) {
		this.cardiovascularDisease = cardiovascularDisease;
	}

	public HeartFailure getHeartFailure() {
		return heartFailure;
	}
	public void setHeartFailure(HeartFailure heartFailure) {
		this.heartFailure = heartFailure;
	}
	public LungDisease getLungDisease() {
		return lungDisease;
	}
	public void setLungDisease(LungDisease lungDisease) {
		this.lungDisease = lungDisease;
	}
	public Diabetes getDiabetes() {
		return diabetes;
	}
	public void setDiabetes(Diabetes diabetes) {
		this.diabetes = diabetes;
	}
	
	

}
