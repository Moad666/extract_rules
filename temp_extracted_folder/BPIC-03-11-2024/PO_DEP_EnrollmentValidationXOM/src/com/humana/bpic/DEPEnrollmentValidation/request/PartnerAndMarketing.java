package com.humana.bpic.DEPEnrollmentValidation.request;

public class PartnerAndMarketing {
	private String partnerApplicationId="";
	private String partnerId="";
	private String partnerName="";
	private String partnerLocationId="";
	private String marketingCampaign="";
	private String marketingLeadSourceCode="";
	public String getPartnerApplicationId() {
		return partnerApplicationId;
	}
	public void setPartnerApplicationId(String partnerApplicationId) {
		this.partnerApplicationId = partnerApplicationId;
	}
	public String getPartnerId() {
		return partnerId;
	}
	public void setPartnerId(String partnerId) {
		this.partnerId = partnerId;
	}
	public String getPartnerName() {
		return partnerName;
	}
	public void setPartnerName(String partnerName) {
		this.partnerName = partnerName;
	}
	public String getPartnerLocationId() {
		return partnerLocationId;
	}
	public void setPartnerLocationId(String partnerLocationId) {
		this.partnerLocationId = partnerLocationId;
	}
	public String getMarketingCampaign() {
		return marketingCampaign;
	}
	public void setMarketingCampaign(String marketingCampaign) {
		this.marketingCampaign = marketingCampaign;
	}
	public String getMarketingLeadSourceCode() {
		return marketingLeadSourceCode;
	}
	public void setMarketingLeadSourceCode(String marketingLeadSourceCode) {
		this.marketingLeadSourceCode = marketingLeadSourceCode;
	}

}
