package com.humana.bpic.DEPEnrollmentValidation.request;

public class AgentsAndBrokers {
	private String agentBrokerId="";
	private String agentBrokerType="";
	private String agentBrokerEmailAddress="";
	private String agentBrokerName="";
	private String agentBrokerSocialSecurityNumber="";
	public String getAgentBrokerId() {
		return agentBrokerId;
	}
	public void setAgentBrokerId(String agentBrokerId) {
		this.agentBrokerId = agentBrokerId;
	}
	public String getAgentBrokerType() {
		return agentBrokerType;
	}
	public void setAgentBrokerType(String agentBrokerType) {
		this.agentBrokerType = agentBrokerType;
	}
	public String getAgentBrokerEmailAddress() {
		return agentBrokerEmailAddress;
	}
	public void setAgentBrokerEmailAddress(String agentBrokerEmailAddress) {
		this.agentBrokerEmailAddress = agentBrokerEmailAddress;
	}
	public String getAgentBrokerName() {
		return agentBrokerName;
	}
	public void setAgentBrokerName(String agentBrokerName) {
		this.agentBrokerName = agentBrokerName;
	}
	public String getAgentBrokerSocialSecurityNumber() {
		return agentBrokerSocialSecurityNumber;
	}
	public void setAgentBrokerSocialSecurityNumber(String agentBrokerSocialSecurityNumber) {
		this.agentBrokerSocialSecurityNumber = agentBrokerSocialSecurityNumber;
	}

	

}
